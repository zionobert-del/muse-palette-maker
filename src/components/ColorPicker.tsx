// Utility function to generate color variations
const generateColorVariations = (baseHue: number, saturationRange: [number, number], lightnessRange: [number, number], count: number): string[] => {
  const colors: string[] = [];
  for (let i = 0; i < count; i++) {
    const saturation = saturationRange[0] + (saturationRange[1] - saturationRange[0]) * (i / (count - 1));
    const lightness = lightnessRange[0] + (lightnessRange[1] - lightnessRange[0]) * (i / (count - 1));
    colors.push(`hsl(${baseHue}, ${Math.round(saturation)}%, ${Math.round(lightness)}%)`);
  }
  return colors;
};

// Add programmatically generated colors to reach 3000+ total
for (let hue = 0; hue < 360; hue += 10) {
  PRESET_COLORS.push(...generateColorVariations(hue, [20, 100], [20, 90], 10));
}

